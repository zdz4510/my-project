<template>
  <div class="standingMaintenance">
    <div class="query">
      <el-form
        :inline="true"
        :model="standingForm"
        ref="standingForm"
        label-width="70px"
      >
        <el-form-item label="产线">
          <el-input
            v-model="standingForm.workCenterRelation"
            placeholder="请输入产线"
          ></el-input>
        </el-form-item>
        <el-form-item label="站位">
          <el-input
            v-model="standingForm.station"
            placeholder="请输入站位"
          ></el-input>
        </el-form-item>
        <el-form-item>
          <el-button size="small" type="primary" @click="handleQuery">
            查询
          </el-button>
          <el-button size="small" type="primary" @click="handleReset">
            重置
          </el-button>
        </el-form-item>
      </el-form>
    </div>
    <div class="operate">
      <el-button size="small" type="primary" @click="handleAdd">
        新增
      </el-button>
      <el-button
        size="small"
        type="primary"
        :disabled="selectionList.length < 1"
        @click="handleEdit"
      >
        修改
      </el-button>
      <el-button
        size="small"
        type="primary"
        :disabled="selectionList.length < 1"
        @click="handleDelete"
      >
        删除
      </el-button>
      <el-button size="small" type="primary" @click="importDialog = true">
        导入
      </el-button>
      <el-button size="small" type="primary" @click="handleExport">
        导出
      </el-button>
    </div>
    <div class="showInfo">
      <el-table
        ref="multipleTable"
        :data="tableData"
        tooltip-effect="dark"
        style="width: 100%"
        @selection-change="handleSelectionChange"
      >
        <el-table-column type="selection" width="55"> </el-table-column>
        <el-table-column prop="station" label="站位" width="60">
        </el-table-column>
        <el-table-column prop="stationDes" label="站位描述" width="100">
        </el-table-column>
        <el-table-column prop="padIp" label="IP地址" width="120">
        </el-table-column>
        <el-table-column prop="resource" label="设备" width="100">
        </el-table-column>
        <el-table-column prop="workCenter" label="工作中心" width="100">
        </el-table-column>
        <el-table-column prop="workCenterRelation" label="产线" width="80">
        </el-table-column>
        <el-table-column prop="status" label="状态" width="60">
        </el-table-column>
        <el-table-column prop="stationType" label="站位类型" width="100">
        </el-table-column>
        <el-table-column prop="fullQty" label="满站数量" width="100">
        </el-table-column>
        <el-table-column prop="name" label="总在制" width="100">
        </el-table-column>
        <el-table-column prop="address" label="站内在制" show-overflow-tooltip>
        </el-table-column>
      </el-table>
    </div>
    <div class="pagination">
      <el-pagination
        background
        layout="->,total,prev,pager,next,sizes"
        :total="total"
        :page-size="pageSize"
        :page-sizes="[5, 10, 15, 20]"
        :current-page="currentPage"
        @size-change="handlePagesize"
        @current-change="handleCurrentChange"
      >
      </el-pagination>
    </div>
    <el-dialog title="导入" :visible.sync="importDialog" width="30%">
      <span>
        <div class="import">
          <el-button size="small" type="primary" @click="downLoad">
            下载导入模板
          </el-button>
        </div>
        <div class="import">
          <el-button size="small" type="primary" @click="upLoad">
            上传导入文件
          </el-button>
        </div>
      </span>
      <span slot="footer" class="dialog-footer">
        <el-button @click="importDialog = false">取消上传</el-button>
        <el-button type="primary" @click="handleImport">
          确定上传
        </el-button>
      </span>
    </el-dialog>
    <el-dialog title="删除" :visible.sync="deleteDialog" width="30%">
      <span>是否确认删除{{ selectionList.length }}条数据？</span>
      <span slot="footer" class="dialog-footer">
        <el-button @click="deleteDialog = false">取 消</el-button>
        <el-button type="primary" @click="handleDelete">
          确 定
        </el-button>
      </span>
    </el-dialog>
  </div>
</template>

<script>
import {
  findPageHttp,
  deleteStationBatchHttp,
  findResourceGroupListHttp
} from "@/api/maintenance/standing.api.js";
import { mapMutations } from "vuex";
import { exportExcel } from "@/until/excel.js";

const tHeader = [
  "站位",
  "站位描述",
  "IP地址",
  "设备",
  "工作中心",
  "产线",
  "状态",
  "站位类型",
  "满站数量",
  "总在制",
  "站内在制"
];
const filterVal = [
  "station",
  "stationDes",
  "padIp",
  "resource",
  "workCenter",
  "workCenterRelation",
  "status",
  "stationType",
  "fullQty",
  //总在制 站内在制暂时没有
  "name",
  "address"
];
const fileName = "站位维护表";
export default {
  data() {
    return {
      tHeader,
      filterVal,
      fileName,
      standingForm: {
        //产线
        workCenterRelation: "",
        //站位
        station: ""
      },
      currentPage: 1,
      pageSize: 10,
      total: 0,
      tableData: [],
      selectionList: [],
      stations: [],
      deleteDialog: false,
      importDialog: false
    };
  },
  created() {
    this.init();
  },
  methods: {
    ...mapMutations(["STANDINGLIST"]),
    //初始化数据
    init() {
      const data = {
        currentPage: this.currentPage,
        pageSize: this.pageSize,
        station: this.standingForm.station,
        workCenterRelation: this.standingForm.workCenterRelation
      };
      findPageHttp(data).then(data => {
        const res = data.data;
        if (res.code === 200) {
          this.total = res.data.total;
          this.tableData = res.data.data;
          return;
        }
        this.$message({
          message: res.message,
          type: "warning"
        });
      });
    },
    //更改当前页码,再次请求数据
    handleCurrentChange(currentChange) {
      this.currentPage = currentChange;
      this.init();
    },
    //更改页码大小
    handlePagesize(pageSize) {
      this.pageSize = pageSize;
      this.currentPage = 1;
      this.init();
    },
    //模糊查询建议
    querySearch() {},
    //模糊查询后选择
    handleSelect() {},
    //复选框选择
    handleSelectionChange(val) {
      this.selectionList = val;
    },
    //查询
    handleQuery() {
      console.log(11);
      this.init();
    },
    //重置
    handleReset() {
      this.standingForm.station = "";
      this.standingForm.workCenterRelation = "";
      this.init();
    },
    //新增
    handleAdd() {
      this.STANDINGLIST([]);
      this.$router.push({
        name: "standingMaintenanceEdit",
        query: { operateType: "add" }
      });
    },
    //编辑
    handleEdit() {
      const tempArr = JSON.parse(JSON.stringify(this.selectionList));
      this.STANDINGLIST(tempArr);
      this.$router.push({
        name: "standingMaintenanceEdit",
        query: { operateType: "edit" }
      });
    },
    handleDelete() {
      const data = this.selectionList;
      deleteStationBatchHttp(data).then(data => {
        const res = data.data;
        if (res.code === 200) {
          this.$message({
            message: "删除成功",
            type: "success"
          });
          this.deleteDialog = false;
          this.init();
          return;
        }
        this.$message({
          message: res.message,
          type: "warning"
        });
        this.deleteDialog = false;
      });
    },
    //确认上传
    handleImport() {},
    //下载模板
    downLoad() {
      findResourceGroupListHttp();
      this.importDialog = false;
    },
    //上传文件
    upLoad() {},
    //未选择导出请求数据
    exportHttp() {
      const request = {
        currentPage: this.currentPage,
        pageSize: 0,
        station: this.standingForm.station,
        workCenterRelation: this.standingForm.workCenterRelation
      };
      findPageHttp(request).then(data => {
        const res = data.data;
        if (res.code === 200) {
          data = res.data.data;
          this.exportResult(data);
          return;
        }
        this.$message({
          message: res.message,
          type: "warning"
        });
      });
    },
    //导出
    handleExport() {
      if (this.selectionList.length === 0) {
        this.exportHttp();
      }
      if (this.selectionList.length > 0) {
        const data = this.selectionList;
        this.exportResult(data);
      }
    },
    //返回结果，提示信息
    exportResult(data) {
      const tipString = exportExcel(tHeader, filterVal, data, this.fileName);
      if (tipString === undefined) {
        this.$message({
          message: "导出成功",
          type: "success"
        });
        return;
      } else {
        this.$message({
          message: tipString,
          type: "warning"
        });
        return;
      }
    }
  }
};
</script>

