<template>
  <div class="working-certificate-maintenance-edit">
    <div class="operate">
      <el-button size="small" type="primary" @click="handleBack">返回</el-button>
      <el-button
        size="small"
        type="primary"
        @click="checkAdd(['workCertFormOne', 'workCertFormTwo'])"
      >保存</el-button>
      <el-button
        size="small"
        type="primary"
        @click="handleReset(['workCertFormOne', 'workCertFormTwo'])"
      >重置</el-button>
    </div>
    <div class="operate">
      <el-form
        :model="workCertificateForm"
        ref="workCertFormOne"
        class="workCertificateFormTop"
        :rules="rules"
        :inline="true"
      >
        <el-form-item label="上岗证" prop="cert">
          <dsn-input
            v-model.trim="workCertificateForm.cert"
            placeholder="请输入上岗证"
            :disabled="isEditStation"
          ></dsn-input>
        </el-form-item>
        <el-form-item label="上岗证描述" prop="certDes">
          <dsn-input v-model.trim="workCertificateForm.certDes" placeholder="请输入上岗证描述"></dsn-input>
        </el-form-item>
      </el-form>
    </div>
    <div class="operate">
      <el-tabs type="border-card">
        <el-tab-pane class="baseInfo">
          <span slot="label">基础信息</span>
          <el-row>
            <el-col :span="12">
              <el-form
                :model="workCertificateForm"
                ref="workCertFormTwo"
                label-width="150px"
                :rules="rules"
              >
                <el-form-item label="状态:" prop="status">
                  <dsn-select
                    v-model="workCertificateForm.status"
                    filterable
                    placeholder="请选择状态"
                    style="width:100%"
                  >
                    <el-option label="已启用" :value="true">已启用</el-option>
                    <el-option label="未启用" :value="false">未启用</el-option>
                  </dsn-select>
                </el-form-item>
                <el-form-item label="站位类型:" prop="certType">
                  <dsn-select
                    v-model="workCertificateForm.certType"
                    filterable
                    placeholder="请选择持续时间类型"
                    @change="selectCertType"
                    style="width:100%"
                  >
                    <el-option label="永久" :value="true">永久</el-option>
                    <el-option label="临时" :value="false">临时</el-option>
                  </dsn-select>
                </el-form-item>
                <el-form-item
                  label="上岗证持续截止日期"
                  prop="certTime"
                  v-if="!workCertificateForm.certType"
                >
                  <el-date-picker
                    v-model="workCertificateForm.certTime"
                    type="datetime"
                    placeholder="选择日期"
                    size="small"
                    value-format="yyyy-MM-dd HH:mm:ss"
                    :picker-options="pickerOptions"
                    style="width:100%"
                  ></el-date-picker>
                </el-form-item>
              </el-form>
            </el-col>
          </el-row>
        </el-tab-pane>
        <el-tab-pane class="defineData">
          <span slot="label">自定义数据</span>
        </el-tab-pane>
      </el-tabs>
    </div>
  </div>
</template>

<script>
import { getAllList } from "@/api/work.center.api.js";
import { saveHttp } from "@/api/maintenance/working.certificate.api.js";
import { mapGetters, mapMutations } from "vuex";
import _ from "lodash";
export default {
  data() {
    return {
      //是否可编辑站位
      isEditStation: false,
      isRequired: false,
      workCertificateForm: {
        cert: "",
        certDes: "",
        status: "",
        certType: "",
        certTime: ""
      },
      workCertFormRules: this.rules,
      workCenters: [],
      workCenterRelations: [],
      pickerOptions: {
        // 限制收货时间不让选择今天以前的
        disabledDate(time) {
          return time.getTime() < Date.now() - 24 * 60 * 60 * 1000;
        }
      }
    };
  },
  computed: {
    ...mapGetters(["workingCertificateList"]),
    rules() {
      return {
        cert: [{ required: true, message: "请输入上岗证", trigger: "change" }],
        status: [{ required: true, message: "请选择状态", trigger: "change" }],
        certType: [
          { required: true, message: "请选择持续时间类型", trigger: "change" }
        ],
        certTime: [
          {
            required: this.isRequired,
            message: "请选择上岗证持续截止日期",
            trigger: "change"
          }
        ]
      };
    }
  },
  watch: {},
  created() {
    this.deBounceSearch();
    this.operateType = this.$route.query.operateType;
    this.cloneList = JSON.parse(JSON.stringify(this.workingCertificateList));
    if (this.operateType === "edit") {
      this.workCertificateForm = JSON.parse(JSON.stringify(this.cloneList[0]));
      this.isEditStation = true;
    }
  },
  methods: {
    ...mapMutations(["WORKINGCERTIFICATELIST"]),
    deBounceSearch() {
      this.fn = _.debounce(cb => {
        const data = { workCenter: this.workCertificateForm.workCenter };
        //查询所有工作中心
        getAllList(data).then(data => {
          const res = data.data;
          if (res.code === 200) {
            this.workCenters = res.data;
            this.workCenters.forEach(element => {
              element.value = element.workCenter;
            });
            cb(this.workCenters);
          } else {
            this.$message({
              message: res.message,
              type: "warning"
            });
          }
        });
      }, 150);
    },
    //搜索
    querySearch(queryString, cb) {
      this.fn(cb);
    },
    // 点击某一行选中后操作的状态你
    handleStationCurrentChange(currentRow) {
      this.workCertificateForm = JSON.parse(JSON.stringify(currentRow));
    },
    //工作中心选择
    handleSelectWorkCenter(item) {
      this.workCertificateForm.workCenter = item.value;
      this.workCenterRelations = item.workCenterRelation;
    },
    //产线选择
    handleSelectWorkCenterRelation(item) {
      this.workCertificateForm.workCenterRelation = item.value;
    },
    //站位类型选择
    selectCertType(val) {
      this.isRequired = val ? true : false;
    },
    handleBack() {
      this.$router.push({ name: "workingCertificateMaintenance" });
    },
    checkAdd(formName) {
      let count = 0;
      formName.forEach(element => {
        this.$refs[element].validate(valid => {
          if (valid) {
            count++;
          } else {
            return false;
          }
        });
      });
      if (count >= 2) {
        this.$confirm("是否保存所选数据?", "保存", {
          confirmButtonText: "确定",
          cancelButtonText: "取消",
          type: "success"
        })
          .then(() => {
            this.handleSave();
          })
          .catch(() => {
            this.$message({
              type: "info",
              message: "已取消保存"
            });
          });
        return;
      }
    },
    //保存
    handleSave() {
      const tempArr = [];
      tempArr.push(this.workCertificateForm);
      let data =
        this.operateType === "add"
          ? { createList: tempArr }
          : { updateList: tempArr };
      saveHttp(data).then(data => {
        const res = data.data;
        if (res.code === 200) {
          this.$message({
            message: "保存成功",
            type: "success"
          });
          this.saveDialog = false;
          this.handleBack();
          return;
        }
        this.$message({
          message: res.message,
          type: "warning"
        });
        this.saveDialog = false;
      });
    },
    //重置
    handleReset(formName) {
      if (this.operateType === "add") {
        formName.forEach(element => {
          this.$refs[element].resetFields();
        });
        return;
      }
      if (this.operateType === "edit") {
        this.workCertificateForm = JSON.parse(
          JSON.stringify(this.cloneList[0])
        );
        return;
      }
    }
  }
};
</script>

