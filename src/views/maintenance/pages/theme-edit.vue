<script>
import { findStationListHttp,updateTopicHttp } from "@/api/theme/theme.api.js";
import { mapGetters, mapMutations } from "vuex";
import _ from "lodash";
export default {
  data() {
    return {
      list: [],
      list2:[],
      allList:{},
      // 左侧未分配数据
      v:'',
      v1:'',
      v2:'',
      // 右侧已分配数据
      w:'',
      w1:'',
      w2:'',
      themeForm: {
        //主题描述
        topicDes: "",
        //主题
        topic: "",
        checked: true
      },
      rules:{
        topic: [{ required: true, message: "主题不能为空", trigger: "blur" }],
      },
      //验证基础信息表单ref
      refArrBaseInfo: ["themeForm", "themeFormTwo"],
      //验证保养配置表单ref
      refArrUpkeepConfig: ["themeForm", "upkeepConfigForm"],
      tableData: [],
      selectionList: [],
      saveType: "design",
      isEditResource: false,
      //预警
      alarmList: [],
      fn: null,
      isRequired: false,
      currentRow: {},
      getTemplate: "",
      leftSelectList: [],
      rightSelectList: [],
      labelLinkList: [],
      labelStorageList: [],
      labelCommand:'',
      previewImage:'',
      // 拷贝当前的
      cloneList:[],
      cloneList2:[]
    };
  },
  computed: {
    ...mapGetters(["themeListGet"])
  },
  created() {
      // console.log(this.themeListGet,"vusx")
      this.themeForm=this.themeListGet[0];
      this.handleTabClick();
  },
  mounted() {
  },
  methods: {
    ...mapMutations(["themeList"]),
    // handleChangeRadio(val) {
    //   this.themeForm.resourceStatus = val;
    //   console.log(val);
    // },
    // 导航栏改变发请求渲染两个表的数据
    handleTabClick() {
      const data=this.themeForm;
      findStationListHttp(data).then(data => {
        const res = data.data;
        if (res.code === 200) {
          this.list2=res.data.currentAllotStations
          this.list = res.data.unAllotStations;
          // 拷贝当前的
          this.cloneList=_.cloneDeep(this.list);
          this.cloneList2=_.cloneDeep(this.list2);
          let obj={
            currentAllotStations: this.list2,
            unAllotStations: this.list
          }
          // 深拷贝查询出来的表格数据
          this.allList= _.cloneDeep(obj)
          return;
        }else{
          this.$message({
            message: res.message,
            type: "warning"
          });
        }
      });
    },
    changeWork() {
      let arr = this.list.filter(item => {
        if(item.workCenterRelation.indexOf(this.v)>=0){
          return  item.workCenterRelation;
        }
      });
      this.list=arr;
    },
    changeWork2() {
      let arr = this.list2.filter(item => {
        if(item.workCenterRelation.indexOf(this.w)>=0){
          return  item.workCenterRelation;
        }
      });
      this.list2=arr;
    },
    changeStation() {
      let arr = this.list.filter(item => {
        if(item.station.indexOf(this.v1)>=0){
          return  item.station;
        }
      });
      this.list=arr;
    },
    changeStation2() {
      let arr = this.list2.filter(item => {
        if(item.station.indexOf(this.w1)>=0){
          return  item.station;
        }
      });
      this.list2=arr;
    },
    changeStationDes() {
      let arr = this.list.filter(item => {
        if(item.stationDes.indexOf(this.v2)>=0){
          return  item.stationDes;
        }
      });
      this.list=arr;
    },
    changeStationDes2() {
      let arr = this.list2.filter(item => {
        if(item.stationDes.indexOf(this.w2)>=0){
          return  item.stationDes;
        }
      });
      this.list2=arr;
    },
    handleReset() {
      this.themeForm=this.themeListGet[0];
      this.handleTabClick();
    },
    // 返回
    handleBack() {
      this.selectionList = [];
      // this.themeList(this.selectionList);
      this.themeForm={};
      this.$router.push({
        name: "themeSafeguard"
      });
    },
    saveHandle(){
      const data= {...this.themeForm};
      data.currentAllotStations	=this.cloneList2;
      updateTopicHttp(data).then(data => {
        const res = data.data;
        if (res.code == 200) {
          this.$message({
            type: "success",
            message: "修改成功"
          });
          this.handleBack();
        } else {
          this.$message({
            type: "error",
            message: res.message
          });
        }
      });
    },
    //  查询子元素
    findItemInArr(item, arr) {
      return arr.filter(subItem => {
        return (
          item.workCenterRelation == subItem.workCenterRelation &&
          item.workCenter == subItem.workCenter
        );
      });
    },
    // 选择的数据移动到左边
    toLeft() {
      // const arr = this.rightSelectList.filter(item => {
      //   return this.findItemInArr(item, this.list).length == 0;
      // });
      // console.log(arr,"222")
      // this.list2.forEach(item=>{
      //   console.log(item,"111")
      // })
      // this.list = [...this.list, ...arr];
      // this.selectionList = [];
      this.list = _.concat(this.list, this.rightSelectList);
      this.list = _.uniq(this.list);
      this.list2 = _.difference(
        this.list2,
        this.rightSelectList
      );
      this.cloneList2= _.cloneDeep(this.list2);
    },
    //选择到数据移动到右边
    toRight() {
      // const arr = this.leftSelectList.filter(item => {
      //   return this.findItemInArr(item, this.list2).length == 0;
      // });
      // this.list2 = [...this.list2, ...arr];
      // this.selectionList = [];
      this.list2 = _.concat(this.list2, this.leftSelectList);
      this.list2 = _.uniq(this.list2);
      this.list = _.difference(this.list, this.leftSelectList);
      this.cloneList2= _.cloneDeep(this.list2);
    },
    handleLeftSelect(s) {
      this.leftSelectList = s;
    },
    handleRightSelect(s) {
      this.rightSelectList = s;
    },
    toTagOpe() {
      this.$router.push("/tag/tagEdit");
    },
  }
};
</script>

