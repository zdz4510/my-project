<template>
  <div class="materialGroup">
    <DsnPanel>
      <div slot="header" class="title clearfix">
        <span>搜索信息</span>
      </div>
      <el-form
        :model="materialGroupForm"
        :rules="groupRules"
        ref="materialGroupForm"
        label-width="100px"
        class="materialGroupForm"
        :inline="true"
      >
        <el-form-item label="物料组" prop="materialGroup">
          <dsn-input v-model.trim="materialGroupForm.materialGroup" placeholder="请输入物料组"></dsn-input>
        </el-form-item>
        <el-form-item>
          <dsn-button size="small" type="primary" icon="el-icon-search" @click.native="handleQuery">查询</dsn-button>
          <dsn-button size="small" type="primary" icon="el-icon-refresh" @click.native="handleReset">重置</dsn-button>
        </el-form-item>
      </el-form>
    </DsnPanel>
    
    <DsnPanel>
      <div slot="header" class="title clearfix">
        <span>搜索结果</span>
      </div>
      <div class="operation">
        <dsn-button size="small" type="success" icon="el-icon-add" @click.native="handleAdd">新增</dsn-button>
        <dsn-button
          size="small"
          type="primary"
          icon="el-icon-edit"
          :disabled="selectionList.length <= 0"
          @click.native="handleEdit"
        >修改</dsn-button>
        <dsn-button
          size="small"
          type="danger"
          icon="el-icon-delete"
          :disabled="selectionList.length <= 0"
          @click.native="deleteDialog = true"
        >删除</dsn-button>
        <dsn-button size="small" type="primary" icon="el-icon-upload2" @click.native="handleExport">导出</dsn-button>
      </div>
      <div class="showInfo">
        <dsn-table
          ref="multipleTable"
          :data="tableData"
          tooltip-effect="dark"
          style="width: 100%"
          height="350px"
          @selection-change="handleSelectionChange"
        >
          <el-table-column type="selection"></el-table-column>
          <el-table-column type="index" label="序号"></el-table-column>
          <el-table-column prop="materialGroup" label="物料组"></el-table-column>
          <el-table-column label="总物料数">
            <template slot-scope="scope">
              <span>{{ scope.row.materialList.length }}</span>
            </template>
          </el-table-column>
          <el-table-column prop="groupDes" label="物料组描述"></el-table-column>
          <el-table-column prop="createUserId" label="创建人"></el-table-column>
          <el-table-column prop="createTime" label="创建时间"></el-table-column>
          <el-table-column prop="modifyUserId" label="修改人"></el-table-column>
          <el-table-column prop="modifyTime" label="修改时间" show-overflow-tooltip></el-table-column>
        </dsn-table>
      </div>
      <div class="pagination">
        <dsn-pagination
          background
          layout="->,total,prev,pager,next,sizes"
          :total="total"
          :page-size="pagesize"
          :page-sizes="[5, 10, 15, 20]"
          :current-page="currentPage"
          @size-change="handlePagesize"
          @current-change="handleCurrentChange"
        ></dsn-pagination>
      </div>
    </DsnPanel>
    <el-dialog title="删除" :visible.sync="deleteDialog" :width="defaltDialogWidth">
      <span>是否确认删除{{ selectionList.length }}条数据？</span>
      <span slot="footer" class="dialog-footer">
        <dsn-button @click.native="deleteDialog = false">取 消</dsn-button>
        <dsn-button type="primary" @click.native="handleDelete">确 定</dsn-button>
      </span>
    </el-dialog>
  </div>
</template>

<script>
import { findPageHttp, deleteHttp } from "@/api/material/material.group.api.js";
import { mapMutations } from "vuex";
import { exportExcel } from "@/until/excel.js";

const tHeader = [
  "物料组",
  "总物料数",
  "物料组描述",
  "创建人",
  "创建时间",
  "修改人",
  "修改时间"
];
const filterVal = [
  "materialGroup",
  "materialTotal",
  "groupDes",
  "createUserName",
  "createTime",
  "modifyUserName",
  "modifyTime"
];
const fileName = "物料组维护表";
export default {
  inject: ["defaltDialogWidth"],
  data() {
    var materialGroupRule = (rule, value, callback) => {
      let reg = /^([A-Z]|[a-z]|[0-9]|_|-|\/)+$/;
      if (!value) {
        callback();
      }
      if (!reg.test(value)) {
        return callback(new Error("只能为字母、数字、-、_、/组成"));
      }
      if ((value + "").length > 30) {
        return callback(new Error("只能输入30位以内"));
      }
      this.materialGroupForm.materialGroup = value.toUpperCase();
      callback();
    };
    return {
      tHeader,
      filterVal,
      fileName,
      materialGroupForm: {
        materialGroup: ""
      },
      tableData: [],
      selectionList: [],
      //分页
      currentPage: 1,
      pagesize: 10,
      total: 0,
      deleteDialog: false,
      groupRules: {
        materialGroup: [
          { required: true, validator: materialGroupRule, trigger: "blur" }
        ],
      },
    };
  },
  created() {
    this.init();
  },
  methods: {
    ...mapMutations(["MATERIALGROUPLIST"]),
    init() {
      const data = {
        currentPage: this.currentPage,
        pageSize: this.pagesize,
        materialGroup: this.materialGroupForm.materialGroup
      };
      findPageHttp(data).then(data => {
        const res = data.data;
        if (res.code === 200) {
          // this.pageShow = true;
          this.total = res.data.total;
          this.tableData = res.data.data;
          // this.materialGroupForm.materialGroup = "";
          return;
        }
        this.$message({
          message: res.message,
          type: "warning"
        });
      });
    },
    toggleSelection(rows) {
      if (rows) {
        rows.forEach(row => {
          this.$refs.multipleTable.toggleRowSelection(row);
        });
      } else {
        this.$refs.multipleTable.clearSelection();
      }
    },
    //当前选中行
    handleSelectionChange(val) {
      this.selectionList = val;
    },
    //更改当前页码,再次请求数据
    handleCurrentChange(currentChange) {
      this.currentPage = currentChange;
      this.init();
    },
    //更改页码大小
    handlePagesize(pagesize) {
      this.pagesize = pagesize;
      this.currentPage = 1;
      this.init();
    },
    handleAdd() {
      this.selectionList = [];
      const emptyObj = { materialGroup: "", groupDes: "" };
      this.selectionList.push(emptyObj);
      this.MATERIALGROUPLIST(this.selectionList);
      this.$router.push({
        // path: "/device/materialGroupEdit",
        name: "materialGroupEdit",
        query: { operateType: "add" }
      });
    },
    handleEdit() {
      this.MATERIALGROUPLIST(this.selectionList);
      this.$router.push({
        // path: "/device/materialGroupEdit",
        name: "materialGroupEdit",
        query: { operateType: "edit" }
      });
    },
    handleDelete() {
      deleteHttp(this.selectionList).then(data => {
        const res = data.data;
        if (res.code === 200) {
          this.$message({
            message: res.message,
            type: "success"
          });
          this.deleteDialog = false;
        }else{
          this.deleteDialog = false;
          this.$message({
            message: res.message,
            type: "warning"
          });
        }
        this.handleReset();
      });
    },
    handleQuery() {
      this.init();
    },
    handleReset() {
      this.materialGroupForm.materialGroup = "";
      this.init();
    },
    //未选择导出请求数据
    exportHttp() {
      const request = {
        currentPage: this.currentPage,
        pageSize: 0,
        materialGroup: this.materialGroupForm.materialGroup
      };
      findPageHttp(request).then(data => {
        const res = data.data;
        if (res.code === 200) {
          data = res.data.data;
          data.forEach(element => {
            element.materialTotal = element.materialList.length;
          });
          this.exportResult(data);
          return;
        }
        this.$message({
          message: res.message,
          type: "warning"
        });
      });
    },
    //导出
    handleExport() {
      if (this.selectionList.length === 0) {
        this.exportHttp();
      }
      if (this.selectionList.length > 0) {
        const data = this.selectionList;
        this.exportResult(data);
      }
    },
    //返回结果，提示信息
    exportResult(data) {
      const tipString = exportExcel(tHeader, filterVal, data, this.fileName);
      if (tipString === undefined) {
        this.$message({
          message: "导出成功",
          type: "success"
        });
        return;
      } else {
        this.$message({
          message: tipString,
          type: "warning"
        });
        return;
      }
    }
  }
};
</script>

